class RotatingGroqClient:
    def __init__(self, keys):
        self.keys = keys
        self.idx = 0
        self.client = Groq(api_key=keys[0])
        print(f"🔑 Groq ready — {len(keys)} keys loaded")

    def _rotate(self):
        self.idx = (self.idx + 1) % len(self.keys)
        self.client = Groq(api_key=self.keys[self.idx])
        print(f"🔄 Rotated to key #{self.idx + 1}")

    def chat(self, messages, model=None, temperature=0.3, max_tokens=2000):
        if model is None:
            model = PRIMARY_MODEL
        for attempt in range(len(self.keys)):
            try:
                resp = self.client.chat.completions.create(
                    model=model,
                    messages=messages,
                    temperature=temperature,
                    max_tokens=max_tokens
                )
                return resp.choices[0].message.content
            except Exception as e:
                print(f"⚠️ Key #{self.idx+1} error: {e}")
                self._rotate()
                time.sleep(1)
        raise Exception("❌ All Groq keys exhausted")

