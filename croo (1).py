from concurrent.futures import ThreadPoolExecutor, as_completed

def execute_plan(plan):
    steps      = plan['execution_plan']
    results    = []
    start      = time.time()

    parallel   = [s for s in steps if s.get('can_run_parallel')]
    sequential = [s for s in steps if not s.get('can_run_parallel')]

    if parallel:
        print(f"\n⚡ Running {len(parallel)} steps in parallel...")
        with ThreadPoolExecutor(max_workers=min(len(parallel), 4)) as ex:
            futures = {ex.submit(execute_single_order, s): s for s in parallel}
            for f in as_completed(futures):
                r = f.result()
                results.append(r)
                icon = '✅' if r['status'] == 'completed' else '❌'
                print(f"   {icon} {r['agent_name']} — {r['status']}")

    if sequential:
        print(f"\n🔗 Running {len(sequential)} steps sequentially...")
        for s in sequential:
            r = execute_single_order(s)
            results.append(r)

    elapsed     = round(time.time() - start, 1)
    completed   = [r for r in results if r['status'] == 'completed']
    total_spent = sum(r['cost_usdc'] for r in completed)

    print(f"\n{'='*44}")
    print(f"📊 Execution Summary")
    print(f"{'='*44}")
    print(f"   Completed : {len(completed)}/{len(steps)}")
    print(f"   Spent     : ${total_spent:.4f} USDC")
    print(f"   Time      : {elapsed}s")
    print(f"{'='*44}")

    return results, total_spent

