import hashlib

def generate_receipt(user_task, plan, results, total_spent, report):
    receipt = {
        'job_id':          f'NEXUS_{int(time.time())}',
        'timestamp':        time.strftime('%Y-%m-%dT%H:%M:%SZ', time.gmtime()),
        'network':         'Base Mainnet',
        'orchestrator':    'NexusAI v1.0',
        'task_summary':     plan['task_summary'],
        'agents_hired': [
            {
                'name':       r['agent_name'],
                'status':     r['status'],
                'cost_usdc':  r['cost_usdc'],
                'tx_hash':    r.get('tx_hash', 'N/A'),
                'mock':       r.get('mock', False)
            }
            for r in results
        ],
        'total_cost_usdc':  total_spent,
        'report_hash':      hashlib.sha256(report.encode()).hexdigest()
    }

    # Tamper-proof receipt hash
    receipt['receipt_hash'] = hashlib.sha256(
        json.dumps(receipt, sort_keys=True).encode()
    ).hexdigest()

    print('\n' + '='*54)
    print('🧾  NEXUSAI JOB RECEIPT')
    print('='*54)
    print(f"  Job ID     : {receipt['job_id']}")
    print(f"  Timestamp  : {receipt['timestamp']}")
    print(f"  Network    : {receipt['network']}")
    print(f"  Task       : {receipt['task_summary']}")
    print(f"\n  Agents hired:")
    for a in receipt['agents_hired']:
        icon = '✅' if a['status'] == 'completed' else '❌'
        tag  = '[MOCK]' if a['mock'] else '[LIVE]'
        print(f"    {icon} {a['name']:<28} ${a['cost_usdc']:.4f} USDC  {tag}")
        print(f"       TX: {a['tx_hash']}")
    print(f"\n  💵 Total paid : ${receipt['total_cost_usdc']:.4f} USDC")
    print(f"  🔐 Report hash: {receipt['report_hash'][:32]}...")
    print(f"  🔐 Receipt hash: {receipt['receipt_hash'][:32]}...")
    print('='*54)

    return receipt

