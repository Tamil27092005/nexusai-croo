async def nexusai_listener(max_polls=5, interval=3):
    """
    Polls CROO for incoming negotiations and handles each one.
    max_polls = how many times to check (set to 999 for production)
    interval  = seconds between polls
    """
    print(f"\n{'='*54}")
    print(f"🎧 NEXUSAI CAP LISTENER — STARTED")
    print(f"{'='*54}")
    print(f"   Polling every {interval}s | Max polls: {max_polls}")
    print(f"   Waiting for incoming jobs...\n")

    seen_ids   = set()
    jobs_done  = 0

    for poll in range(max_polls):
        try:
            # Check for pending negotiations where NexusAI is the provider
            pending = await croo_client.list_negotiations(
                ListOptions(role="provider", page=1, page_size=10)
            )

            new_jobs = [
                n for n in pending
                if (n.get('negotiation_id') or n.get('id')) not in seen_ids
                and n.get('status', '') in ['pending', 'open', 'requested', '']
            ]

            if new_jobs:
                print(f"📬 Poll {poll+1}: {len(new_jobs)} new job(s) found")
                for job in new_jobs:
                    job_id = job.get('negotiation_id') or job.get('id')
                    seen_ids.add(job_id)
                    await handle_incoming_job(job)
                    jobs_done += 1
            else:
                print(f"   Poll {poll+1}/{max_polls} — no new jobs, waiting...")

        except Exception as e:
            print(f"   ⚠️ Poll {poll+1} error: {e}")

        if poll < max_polls - 1:
            await asyncio.sleep(interval)

    print(f"\n{'='*54}")
    print(f"🏁 Listener stopped")
    print(f"   Jobs processed : {jobs_done}")
    print(f"   Polls completed: {max_polls}")
    print(f"{'='*54}")

